package com.itheima.constsnt;

public class ConstantDemo1 {
    public static void main(String[] args) {
        //打印李云龙=>通过分析：字符串常量（被""包裹的内容）直接把这个内容放到输出语句中即可.
        System. out.println("李云龙");
        System. out.println('男');
        System. out.println(1.78);
        System. out.println(74);
//打印已婚=>通过分析：（用true表示结婚了/用false表示没结婚）
        System. out.println(true);

//注意：空常量null不可以被打印.
    }
}

