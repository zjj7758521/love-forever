package com.itheima.variable;

public class VariableDemo2 {
    public static void main(String[] args) {
        //数量的定义格式（2）:先声明再赋值.  声明：在内存中有一块空间叫指定的名称，具有指定的数据类型 （声明的时候里面是没有值的）
        //格式：数据类型  变量名； =>  变量名=初始化值；

        int age ;
        age = 30;

        System.out.println("年龄是:" + age);
    }
}
